# Decrease Pattern
for i in range(6,1,-1):  
    for j in range(1, i):   
        print(j, end = ' ')
    print()

# Increase Pattern
for i in range(1,6):
    for j in range(1, i+1):
        print(j, end = ' ')
    print()                

